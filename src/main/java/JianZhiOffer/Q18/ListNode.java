public class ListNode {
    int value;
    ListNode nextNode;

    @Override
    public String toString() {
        return "ListNode{" +
                "value=" + value +
                ", nextNode=" + nextNode +
                '}';
    }

    public ListNode() {
    }

    public ListNode(int value) {
        this.value = value;
    }
}
